#import "MatchState.h"


@implementation MatchState

@synthesize homeScore;
@synthesize awayScore;

- (id) init
{
	if(self = [super init])
	{
		homeScore = 0;
		awayScore = 0;
	}
	
	return self;
}

- (void) dealloc
{
	[super dealloc];
}

@end
